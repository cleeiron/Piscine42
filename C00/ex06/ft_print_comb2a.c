#include <unistd.h>

void ft_print_comb2a(void)
{
    char a1 = '0';
    char a2 = '0';
    char b1 = '0';
    char b2 = '0';

    while(a1 <= '9')
    {
        while(a2 <= '8')
        {
            b1 = a1;
            b2 = a2+1;
            while(b1 <= '9')
            {
                while(b2 <= '9')
                {
                    write(1, &a1, 1);
                    write(1, &a2, 1);
                    write(1, " ", 1);
                    write(1, &b1, 1);
                    write(1, &b2, 1);
                    if(!(a1 == '9' && a2 == '8' && b1 == '9' && b2 == '9'))
                    write(1, ", ", 2);
                    b2++;
                }
                b1++;
                b2 = '0';
            }
            a2++;
        }
        a1++;
        a2 = '0';
    }
}
int main()
{
    ft_print_comb2a();
    return 0;
}