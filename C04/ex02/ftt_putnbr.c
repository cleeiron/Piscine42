#include <unistd.h>

void ft_putnbr(int n)
{
    if(n == -2147483648)
        write(1, "-2147483648",12);
    
    if(n < 0)
        write(1, "-", 1);
    
    if(n >= 0 && n <= 9)
    {
        n = n +'0';
        write(1, &n, 1);     
    }
    
    else
    {
        ft_putnbr(n / 10);
        ft_putnbr(n % 10);
    }
}

int main(void)
{
    ft_putnbr(66);
    return 0;
}